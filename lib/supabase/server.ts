// Service Role Client for admin actions (use with caution)
export function createSupabaseAdminClient() {
  if (!process.env.SUPABASE_SERVICE_ROLE_KEY) {
    throw new Error('SUPABASE_SERVICE_ROLE_KEY is not defined in environment variables.');
  }
  return createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.SUPABASE_SERVICE_ROLE_KEY!,
    {
      cookies: {
        get(name: string) {
          return undefined; // Or however you want to handle this for admin tasks
        },
        set(name: string, value: string, options: CookieOptions) {
          // Admin client typically doesn't set user cookies
        },
        remove(name: string, options: CookieOptions) {
          // Admin client typically doesn't remove user cookies
        },
      },
    }
  );
}
